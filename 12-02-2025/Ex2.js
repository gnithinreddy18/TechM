x=prompt("Enter a marks");
if(x>90){
    document.write("O");
}
else if(x>80){
    document.write("A+");
}
else if(x>70){
    document.write("A");
}
else if(x>60){
    document.write("B+");
}
else if(x>50){
    document.write("B");
}
else if(x>35){
    document.write("C");
}
else{
    document.write("F");
}