function click1(){
    let x=document.getElementById("c1");
    if(x.style.color=="red"){
        x.style.color="yellow";
    }
    else if(x.style.color=="yellow"){
        x.style.color="cyan";
    }
    else{
        x.style.color="red";
    }
}
